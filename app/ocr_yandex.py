from pathlib import Path
import re


def normalize_text(s: str) -> str:
    s = (s or "").strip().lower()
    s = s.replace("ё", "е")
    s = s.replace("«", '"').replace("»", '"')
    s = re.sub(r"\s+", " ", s)
    return s


